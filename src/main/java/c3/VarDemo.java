package c3;

public class VarDemo {
    public static void main(String[] args) {
        var avg = 10.0;
        System.out.println("Value of avg is "+avg);

        int var = 1;
        System.out.println("Value of var is "+var);

        var k = -var;
        System.out.println("Value of k is "+k);


    }
}
