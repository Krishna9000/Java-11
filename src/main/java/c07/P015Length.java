package c07;

public class P015Length {
    public static void main(String[] args) {
        int a[] = new int[10];
        int b[] = {3, 5, 7, 1, 8, 99, 44, -10};
        int c[] = {4, 3, 2, 1};

        System.out.println("Length of a is "+a.length);
        System.out.println("Length of b is "+b.length);
        System.out.println("Length of c is "+c.length);
    }
}
